        <p>若您有任何疑问，请随时咨询将此信交在您手里的弟兄/姐妹，或直接联系灯塔教会福音宣教部负责人陈晓霞姊妹（手机：+86 18833876209 微信Wechat：RitaChenQHD）。</p>
